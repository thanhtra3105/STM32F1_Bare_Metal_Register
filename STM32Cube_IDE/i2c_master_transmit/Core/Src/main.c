void ds3231_setTime(uint8_t se,uint8_t mi,uint8_t ho,uint8_t wd,uint8_t d,uint8_t m,uint8_t y)
{
	uint8_t buffer[8] = {0x00,dec2bcd(se),dec2bcd(mi),dec2bcd(ho),dec2bcd(wd),dec2bcd(d),dec2bcd(m),dec2bcd(y)};
	HAL_I2C_Master_Transmit(&hi2c1, DS3231_ADDRESS<<1, buffer, 8, HAL_MAX_DELAY);
}
void ds3231_read()
{
	uint8_t reg = 0x00;
	uint8_t buffer[7] = {0};
	HAL_I2C_Master_Transmit(&hi2c1, DS3231_ADDRESS <<1, &reg, 1, HAL_MAX_DELAY);
	HAL_I2C_Master_Receive(&hi2c1, (DS3231_ADDRESS<<1)|0x01, buffer, 7, HAL_MAX_DELAY);
	ds3231.second 	= bcd2dec(buffer[0]);
	ds3231.minute 	= bcd2dec(buffer[1]);
	ds3231.hour   	= bcd2dec(buffer[2] & 0x3f);
	ds3231.weekDay 	= bcd2dec(buffer[3]);
	ds3231.day 		= bcd2dec(buffer[4]);
	ds3231.month 	= bcd2dec(buffer[5]);
	ds3231.year 	= bcd2dec(buffer[6]);
}
uint8_t bcd2dec(uint8_t bcd)
{
	return (bcd>>4)*10 + (bcd&0x0F);
}
uint8_t dec2bcd(uint8_t dec)
{
	return ((dec/10)<<4)+(dec%10);
}
